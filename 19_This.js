// What is 'this' keyword?
// 'this' keyword refers to the object that is executing the current function,
// but it's value depends on how the function is called, not where it is defined.

// it has a different values depending on where it is used.

// 1. Alone, this referes to the global object that is "Window object";

// 2. In a regular function , 'this' refers to the global object

// 3. In a method, 'this' refers to the owner object 
// 4. In a constructor, 'this' refers to the newly created object
// 5. In an arrow function, 'this' refers to the global object
// 6. In a class method, 'this' refers to the class itself
// 7. In a class static method, 'this' is undefined

